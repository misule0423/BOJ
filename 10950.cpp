#include <iostream>
#include <cstdio>

int main(int argc, const char * argv[]) {
    
    int t;
    
    scanf("%d", &t);
    
    int a, b;
    
    while (t--) {
        
        scanf("%d %d", &a, &b);
        
        printf("%d\n", a+b);
        
    }
    
    return 0;
}
